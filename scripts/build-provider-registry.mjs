/**
 * @file
 * @brief Generate the shared layer's **static indexes** — the ONE form of plugin/asset discovery that
 * works in all three runtimes. Currently two:
 *
 *   - `targets/shared/registry.generated.js` — the provider registry.
 *   - `targets/shared/theme/presets.generated.js` — the built-in accent themes, inlined from
 *     `targets/shared/theme/themes/*.json`. (The DATA is inlined as JS rather than `import`ed from the
 *     JSON, because a bare JSON import needs an `import` attribute in Node but not in Vite/Metro —
 *     inlining sidesteps the whole disagreement.)
 *
 * ## Why this exists
 *
 * The provider folders (`targets/shared/<id>/`) are a drop-a-folder-in plugin pool, which needs a
 * *discovery* mechanism. Each runtime has a different one, and none of them is universal:
 *
 *   - **Vite** (web target): `import.meta.glob` — build-time, works only under Vite.
 *   - **Node** (CLI / backend): `fs.readdirSync` + dynamic `import()` — no `import.meta.glob`.
 *   - **Metro** (mobile target): neither. Metro resolves a **static** module graph; it has no glob
 *     and no filesystem at runtime.
 *
 * Historically that produced THREE registries over the same 41 folders (the Vite glob, a
 * fs-discovery re-port in the CLI, and an 892-line hand-port in the mobile target) — three copies
 * that could silently drift, which is exactly what the parity checks were papering over.
 *
 * A **generated static index** is the one form all three runtimes understand: plain `import`
 * statements. This script writes it, so adding a provider folder is still zero-central-edit — you
 * just re-run the generator (and `npm run check:registry`, wired into `npm test`, fails if you
 * forgot).
 *
 * Usage:
 *   node scripts/build-provider-registry.mjs           # write the file
 *   node scripts/build-provider-registry.mjs --check    # verify it's up to date (CI gate), exit 1 if not
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { format, resolveConfig } from "prettier";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sharedDir = path.join(repoRoot, "targets", "shared");
const outFile = path.join(sharedDir, "registry.generated.js");

/** A JS identifier safe to use as an import binding (provider ids contain `-`). */
const ident = (id) => id.replace(/[^A-Za-z0-9]/g, "_");

/** Every provider folder: a direct child of `targets/shared/` (minus `_`-prefixed) with a config.js. */
function findProviders() {
  return fs
    .readdirSync(sharedDir, { withFileTypes: true })
    .filter((d) => d.isDirectory() && !d.name.startsWith("_"))
    .map((d) => d.name)
    .filter((name) => fs.existsSync(path.join(sharedDir, name, "config.js")))
    .sort();
}

/** Every shared-setting descriptor module: `_shared/settings/*.js` minus the index. */
function findSharedSettings() {
  const dir = path.join(sharedDir, "_shared", "settings");
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((n) => n.endsWith(".js") && n !== "index.js")
    .map((n) => n.replace(/\.js$/, ""))
    .sort();
}

function render() {
  const providers = findProviders();
  const settings = findSharedSettings();

  const lines = [
    "/**",
    " * AUTO-GENERATED by `scripts/build-provider-registry.mjs` — DO NOT EDIT BY HAND.",
    " *",
    " * The static provider index. Vite, Node and Metro all understand plain `import` statements, so",
    " * this generated file is the single discovery mechanism shared by every target (see the",
    " * generator's header for why a glob / fs-scan can't be). Run `npm run registry` after adding or",
    " * removing a provider folder; `npm run check:registry` (in `npm test`) fails if it's stale.",
    " *",
    " * @module targets/shared/registry.generated",
    " */",
    "",
    ...providers.map((id) => `import ${ident(id)} from "./${id}/config.js";`),
    ...settings.map((n) => `import setting_${ident(n)} from "./_shared/settings/${n}.js";`),
    "",
    "/** @type {object[]} Every provider manifest (each folder's `config.js` default export). */",
    "export const PROVIDER_CONFIGS = [",
    ...providers.map((id) => `  ${ident(id)},`),
    "];",
    "",
    "/** @type {object[]} Every shared-setting descriptor (`_shared/settings/*.js`). */",
    "export const SHARED_SETTING_DESCRIPTORS = [",
    ...settings.map((n) => `  setting_${ident(n)},`),
    "];",
    "",
  ];
  return lines.join("\n");
}

// ---------------------------------------------------------------------------------------------
// The theme index: the built-in accent themes, inlined from targets/shared/theme/themes/*.json.
// ---------------------------------------------------------------------------------------------

const themesDir = path.join(sharedDir, "theme", "themes");
const themeOutFile = path.join(sharedDir, "theme", "presets.generated.js");

function renderThemes() {
  // The NN- filename prefix defines picker order, so a plain filename sort yields the intended order.
  const files = fs
    .readdirSync(themesDir)
    .filter((f) => f.endsWith(".json"))
    .sort();
  const accents = files.map((f) => JSON.parse(fs.readFileSync(path.join(themesDir, f), "utf8")));

  return [
    "/**",
    " * AUTO-GENERATED by `scripts/build-provider-registry.mjs` — DO NOT EDIT BY HAND.",
    " *",
    " * The built-in accent themes, in picker order, inlined from `./themes/*.json` (the JSON file IS",
    " * the theme — edit those, then run `npm run registry`). The data is inlined as plain JS rather",
    " * than imported from the JSON so it loads identically under Vite, Node AND Metro (a bare JSON",
    " * import needs an `import` attribute in Node but not in the others).",
    " *",
    " * The web target used to discover these with a Vite glob, which Metro can't do — so the mobile",
    " * target hand-copied all nine themes into `lib/themeData.js`, guarded by a drift check. Same story",
    " * as the providers; same fix. See `notes/plans/de-duplication.md`.",
    " *",
    " * @module targets/shared/theme/presets.generated",
    " */",
    "",
    "/** @type {object[]} The built-in accent themes, in filename (picker) order. */",
    `export const ACCENTS = ${JSON.stringify(accents, null, 2)};`,
    "",
  ].join("\n");
}

// Run the rendered source through Prettier before writing OR comparing. Without this the generator's
// output was hand-assembled strings that happened not to be Prettier-clean, so `npm run format:check`
// went red on a file nobody is allowed to edit by hand — and the only way to fix it (formatting the
// file) made `check:registry` call it STALE. Two gates, each demanding the other be broken. Formatting
// at the source settles it: the file the generator writes is the file both gates expect.
const prettierOpts = { ...(await resolveConfig(outFile)), parser: "babel" };
const next = await format(render(), prettierOpts);
const nextThemes = await format(renderThemes(), prettierOpts);
const check = process.argv.includes("--check");
// Read it, don't ASK whether it exists and then read it. `existsSync` + `readFileSync` is a
// check-then-use race (CodeQL js/file-system-race): the file can vanish between the two calls, and the
// "safe" version is the one that just tries. A missing file is not an error here — it means "not
// generated yet".
const readIfPresent = (p) => {
  try {
    return fs.readFileSync(p, "utf8");
  } catch (e) {
    if (e.code === "ENOENT") return null;
    throw e;
  }
};
const prev = readIfPresent(outFile);
const prevThemes = readIfPresent(themeOutFile);

if (check) {
  if (prev !== next) {
    console.error(
      `check:registry: targets/shared/registry.generated.js is STALE.\n` +
        `A provider folder (or shared setting) was added/removed without regenerating.\n` +
        `Fix: npm run registry`,
    );
    process.exit(1);
  }
  if (prevThemes !== nextThemes) {
    console.error(
      `check:registry: targets/shared/theme/presets.generated.js is STALE.\n` +
        `A theme file was added/changed/removed without regenerating.\n` +
        `Fix: npm run registry`,
    );
    process.exit(1);
  }
  const n = (next.match(/^import \w+ from "\.\/[^_]/gm) || []).length;
  console.log(
    `check:registry: OK — registry.generated.js (${n} providers) + theme/presets.generated.js are up to date.`,
  );
} else {
  fs.writeFileSync(outFile, next);
  fs.writeFileSync(themeOutFile, nextThemes);
  const providers = findProviders();
  console.log(
    `registry: wrote registry.generated.js (${providers.length} providers, ${findSharedSettings().length} shared settings) + theme/presets.generated.js.`,
  );
}
